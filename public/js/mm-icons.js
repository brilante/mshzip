  /**
   * SVG 아이콘 HTML 생성
   * @param {string} name - 아이콘 이름
   * @param {number} [size=16] - 크기 (px)
   * @param {string} [className=''] - 추가 CSS 클래스
   * @returns {string} SVG HTML 문자열
   */
  function mmIcon(name, size, className) {
    size = size || 16;
    className = className ? ('mm-icon ' + className) : 'mm-icon';

    // fill 기반 브랜드 아이콘 확인
    const fillIcon = FILL_ICONS[name];
    if (fillIcon) {
      return '<svg class="' + className + '" width="' + size + '" height="' + size +
        '" viewBox="' + fillIcon.viewBox + '" fill="currentColor">' +
        fillIcon.paths + '</svg>';
    }

    // stroke 기반 Feather/Lucide 아이콘
    const paths = ICONS[name];
    if (!paths) {
      console.warn('[mm-icons] 아이콘을 찾을 수 없음:', name);
      return '<span class="mm-icon-fallback">?</span>';
    }
    return '<svg class="' + className + '" width="' + size + '" height="' + size +
      '" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2.5" stroke-linecap="round" stroke-linejoin="round">' +
      paths + '</svg>';
  }

  /**
   * DOM 요소로 아이콘 생성
   * @param {string} name - 아이콘 이름
   * @param {number} [size=16] - 크기
   * @param {string} [className=''] - 추가 CSS 클래스
   * @returns {SVGElement}
   */
  function mmIconEl(name, size, className) {
    const wrapper = document.createElement('span');
    wrapper.innerHTML = mmIcon(name, size, className);
    return wrapper.firstChild;
  }

  /**
   * data-icon 속성 자동 교체
   * <span data-icon="search" data-icon-size="16"></span> → SVG 아이콘
   */
  function initDataIcons() {
    document.querySelectorAll('[data-icon]').forEach(function(el) {
      var name = el.getAttribute('data-icon');
      var size = parseInt(el.getAttribute('data-icon-size') || '16', 10);
      el.innerHTML = mmIcon(name, size);
      el.removeAttribute('data-icon');
    });
  }

  // 전역 등록
  window.mmIcon = mmIcon;
  window.mmIconEl = mmIconEl;
  window.MM_ICONS = ICONS;
  window.MM_FILL_ICONS = FILL_ICONS;
  window.mmInitDataIcons = initDataIcons;

  // DOMContentLoaded 시 자동 교체
  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', initDataIcons);
  } else {
    initDataIcons();
  }
