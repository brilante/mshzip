var SitemapGenerator = class SitemapGenerator extends ToolBase {
  constructor() {
    super('SitemapGenerator');
  }

  init() {
    this.initElements({
      urlList: 'urlList',
      urlCount: 'urlCount',
      defaultFreq: 'defaultFreq',
      defaultPriority: 'defaultPriority',
      includeLastmod: 'includeLastmod',
      includeFreq: 'includeFreq',
      includePriority: 'includePriority',
      sitemapOutput: 'sitemapOutput',
      robotsOutput: 'robotsOutput',
      sitemapUrl: 'sitemapUrl',
      allowAll: 'allowAll'
    });

    this.generateRobots();
    console.log('[SitemapGenerator] 초기화 완료');
    return this;
  }

  getUrls() {
    const text = this.elements.urlList.value.trim();
    if (!text) return [];

    return text.split('\n')
      .map(url => url.trim())
      .filter(url => url && this.isValidUrl(url));
  }

  isValidUrl(string) {
    try {
      new URL(string);
      return true;
    } catch (_) {
      return false;
    }
  }

  generate() {
    const urls = this.getUrls();
    this.elements.urlCount.textContent = urls.length;

    if (urls.length === 0) {
      this.elements.sitemapOutput.innerHTML = '<code>&lt;!-- URL을 입력하면 사이트맵이 생성됩니다 --&gt;</code>';
      return;
    }

    const freq = this.elements.defaultFreq.value;
    const priority = this.elements.defaultPriority.value;
    const includeLastmod = this.elements.includeLastmod.checked;
    const includeFreq = this.elements.includeFreq.checked;
    const includePriority = this.elements.includePriority.checked;

    const today = new Date().toISOString().split('T')[0];

    let xml = '<?xml version="1.0" encoding="UTF-8"?>\n';
    xml += '<urlset xmlns="http://www.sitemaps.org/schemas/sitemap/0.9">\n';

    urls.forEach((url, index) => {
      xml += '  <url>\n';
      xml += `    <loc>${this.escapeXml(url)}</loc>\n`;

      if (includeLastmod) {
        xml += `    <lastmod>${today}</lastmod>\n`;
      }
      if (includeFreq) {
        xml += `    <changefreq>${freq}</changefreq>\n`;
      }
      if (includePriority) {
        // 첫 번째 URL은 가장 높은 우선순위
        const urlPriority = index === 0 ? '1.0' : priority;
        xml += `    <priority>${urlPriority}</priority>\n`;
      }

      xml += '  </url>\n';
    });

    xml += '</urlset>';

    this.elements.sitemapOutput.innerHTML = `<code>${this.escapeHtml(xml)}</code>`;
  }

  generateRobots() {
    const sitemapUrl = this.elements.sitemapUrl.value.trim();
    const allowAll = this.elements.allowAll.checked;

    let robots = '# robots.txt\n';
    robots += '# Generated by MyMind3 Sitemap Generator\n\n';

    robots += 'User-agent: *\n';

    if (allowAll) {
      robots += 'Allow: /\n';
    } else {
      robots += 'Disallow: /\n';
    }

    robots += '\n';

    if (sitemapUrl) {
      robots += `Sitemap: ${sitemapUrl}\n`;
    } else {
      robots += '# Sitemap: https://example.com/sitemap.xml\n';
    }

    this.elements.robotsOutput.innerHTML = `<code>${this.escapeHtml(robots)}</code>`;
  }

  addSampleUrls() {
    const sample = `https://example.com/
https://example.com/about
https://example.com/services
https://example.com/contact
https://example.com/blog
https://example.com/blog/post-1
https://example.com/blog/post-2`;

    this.elements.urlList.value = sample;
    this.generate();
    this.showToast('샘플 URL이 추가되었습니다.');
  }

  clearUrls() {
    this.elements.urlList.value = '';
    this.generate();
    this.showToast('URL 목록이 삭제되었습니다.');
  }

  escapeXml(text) {
    return text
      .replace(/&/g, '&amp;')
      .replace(/</g, '&lt;')
      .replace(/>/g, '&gt;')
      .replace(/"/g, '&quot;')
      .replace(/'/g, '&apos;');
  }

  escapeHtml(text) {
    const div = document.createElement('div');
    div.textContent = text;
    return div.innerHTML;
  }

  async copy(type) {
    const element = type === 'sitemap' ? this.elements.sitemapOutput : this.elements.robotsOutput;
    const code = element.textContent;

    if (code.includes('URL을 입력하면') || code.includes('robots.txt 미리보기')) {
      this.showToast('복사할 내용이 없습니다.', 'error');
      return;
    }

    try {
      await navigator.clipboard.writeText(code);
      this.showToast(`${type === 'sitemap' ? '사이트맵' : 'robots.txt'}이 복사되었습니다.`);
    } catch (err) {
      this.showToast('복사에 실패했습니다.', 'error');
    }
  }

  download(type) {
    const element = type === 'sitemap' ? this.elements.sitemapOutput : this.elements.robotsOutput;
    const code = element.textContent;

    if (code.includes('URL을 입력하면') || code.includes('robots.txt 미리보기')) {
      this.showToast('다운로드할 내용이 없습니다.', 'error');
      return;
    }

    const filename = type === 'sitemap' ? 'sitemap.xml' : 'robots.txt';
    const mimeType = type === 'sitemap' ? 'application/xml' : 'text/plain';

    const blob = new Blob([code], { type: mimeType });
    const link = document.createElement('a');
    link.download = filename;
    link.href = URL.createObjectURL(blob);
    link.click();
    URL.revokeObjectURL(link.href);

    this.showToast(`${filename} 파일이 다운로드되었습니다.`);
  }
}

// 전역 인스턴스 생성 (onclick에서 사용)
const sitemapGenerator = new SitemapGenerator();
window.SitemapGenerator = sitemapGenerator;

document.addEventListener('DOMContentLoaded', () => sitemapGenerator.init());
