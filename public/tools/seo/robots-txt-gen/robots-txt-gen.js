/**
 * robots.txt 생성기 - ToolBase 기반
 * 검색 엔진 크롤러 설정 파일 생성
 * @created 2026-01-12
 * @updated 2026-01-24 ToolBase 마이그레이션
 */

var RobotsTxtGen = class RobotsTxtGen extends ToolBase {
  constructor() {
    super('RobotsTxtGen');
    this.rules = [];
  }

  init() {
    this.initElements({
      rulesContainer: 'rulesContainer',
      sitemapUrl: 'sitemapUrl',
      crawlDelay: 'crawlDelay',
      hostUrl: 'hostUrl',
      outputCode: 'outputCode'
    });

    this.addRule();

    console.log('[RobotsTxtGen] 초기화 완료');
    return this;
  }

  addRule() {
    const id = Date.now();
    this.rules.push({ id, userAgent: '*', allow: [], disallow: [] });
    this.renderRules();
    this.generate();
  }

  removeRule(id) {
    this.rules = this.rules.filter(r => r.id !== id);
    if (this.rules.length === 0) this.addRule();
    else {
      this.renderRules();
      this.generate();
    }
  }

  renderRules() {
    this.elements.rulesContainer.innerHTML = this.rules.map((rule, idx) => `
      <div class="rule-block" data-id="${rule.id}">
        <div class="rule-header">
          <span>규칙 ${idx + 1}</span>
          <button class="remove-btn" onclick="robotsTxtGen.removeRule(${rule.id})">×</button>
        </div>
        <div class="control-group">
          <label>User-Agent</label>
          <select class="tool-input" onchange="robotsTxtGen.updateUserAgent(${rule.id}, this.value)">
            <option value="*" ${rule.userAgent === '*' ? 'selected' : ''}>모든 봇 (*)</option>
            <option value="Googlebot" ${rule.userAgent === 'Googlebot' ? 'selected' : ''}>Googlebot</option>
            <option value="Bingbot" ${rule.userAgent === 'Bingbot' ? 'selected' : ''}>Bingbot</option>
            <option value="Yandex" ${rule.userAgent === 'Yandex' ? 'selected' : ''}>Yandex</option>
            <option value="Baiduspider" ${rule.userAgent === 'Baiduspider' ? 'selected' : ''}>Baiduspider</option>
            <option value="DuckDuckBot" ${rule.userAgent === 'DuckDuckBot' ? 'selected' : ''}>DuckDuckBot</option>
            <option value="facebookexternalhit" ${rule.userAgent === 'facebookexternalhit' ? 'selected' : ''}>Facebook</option>
            <option value="Twitterbot" ${rule.userAgent === 'Twitterbot' ? 'selected' : ''}>Twitter</option>
          </select>
        </div>
        <div class="control-group">
          <label>Disallow (차단할 경로)</label>
          <input type="text" class="tool-input" placeholder="/admin/, /private/"
            value="${rule.disallow.join(', ')}"
            oninput="robotsTxtGen.updatePaths(${rule.id}, 'disallow', this.value)">
        </div>
        <div class="control-group">
          <label>Allow (허용할 경로)</label>
          <input type="text" class="tool-input" placeholder="/public/, /api/"
            value="${rule.allow.join(', ')}"
            oninput="robotsTxtGen.updatePaths(${rule.id}, 'allow', this.value)">
        </div>
      </div>
    `).join('');
  }

  updateUserAgent(id, value) {
    const rule = this.rules.find(r => r.id === id);
    if (rule) {
      rule.userAgent = value;
      this.generate();
    }
  }

  updatePaths(id, type, value) {
    const rule = this.rules.find(r => r.id === id);
    if (rule) {
      rule[type] = value.split(',').map(p => p.trim()).filter(p => p);
      this.generate();
    }
  }

  generate() {
    const sitemap = this.elements.sitemapUrl.value.trim();
    const crawlDelay = this.elements.crawlDelay.value;
    const host = this.elements.hostUrl.value.trim();

    let output = '# robots.txt\n# Generated by MyMind3 Tools\n\n';

    this.rules.forEach(rule => {
      output += `User-agent: ${rule.userAgent}\n`;

      rule.disallow.forEach(path => {
        output += `Disallow: ${path}\n`;
      });

      rule.allow.forEach(path => {
        output += `Allow: ${path}\n`;
      });

      if (crawlDelay && crawlDelay !== '0') {
        output += `Crawl-delay: ${crawlDelay}\n`;
      }

      output += '\n';
    });

    if (sitemap) {
      output += `Sitemap: ${sitemap}\n`;
    }

    if (host) {
      output += `Host: ${host}\n`;
    }

    this.elements.outputCode.textContent = output;
  }

  useTemplate(type) {
    this.rules = [];

    switch (type) {
      case 'allow-all':
        this.rules.push({ id: Date.now(), userAgent: '*', allow: [], disallow: [] });
        break;
      case 'block-all':
        this.rules.push({ id: Date.now(), userAgent: '*', allow: [], disallow: ['/'] });
        break;
      case 'standard':
        this.rules.push({
          id: Date.now(),
          userAgent: '*',
          allow: [],
          disallow: ['/admin/', '/private/', '/api/', '/tmp/', '/*.json$']
        });
        break;
      case 'wordpress':
        this.rules.push({
          id: Date.now(),
          userAgent: '*',
          allow: ['/wp-admin/admin-ajax.php'],
          disallow: ['/wp-admin/', '/wp-includes/', '/wp-content/plugins/', '/trackback/', '/feed/', '/comments/']
        });
        break;
    }

    this.renderRules();
    this.generate();
    this.showToast('템플릿 적용됨!', 'success');
  }

  async copy() {
    const code = this.elements.outputCode.textContent;
    const success = await this.copyToClipboard(code);
    this.showToast(success ? '복사됨!' : '복사 실패', success ? 'success' : 'error');
  }

  download() {
    const code = this.elements.outputCode.textContent;
    const blob = new Blob([code], { type: 'text/plain' });
    const url = URL.createObjectURL(blob);
    const a = document.createElement('a');
    a.href = url;
    a.download = 'robots.txt';
    a.click();
    URL.revokeObjectURL(url);
    this.showToast('다운로드 완료!', 'success');
  }
}

// 전역 인스턴스 생성
const robotsTxtGen = new RobotsTxtGen();
window.RobotsTxtGen = robotsTxtGen;

document.addEventListener('DOMContentLoaded', () => robotsTxtGen.init());
